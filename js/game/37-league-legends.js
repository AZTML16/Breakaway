function getLeagueHistoricalLegends(leagueKey){
  var lk=leagueKey||(typeof G!=='undefined'&&G?G.leagueKey:'');
  var legs=LEAGUE_HISTORICAL_LEGENDS[lk];
  if(legs&&legs.length) return legs;
  if(typeof getMenAnalogLeagueKey==='function'){
    var analog=getMenAnalogLeagueKey(lk);
    if(analog&&analog!==lk&&LEAGUE_HISTORICAL_LEGENDS[analog]) return LEAGUE_HISTORICAL_LEGENDS[analog];
  }
  return [];
}

