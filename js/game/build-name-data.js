/* Run: node js/game/build-name-data.js */
var fs = require('fs');
var path = require('path');
var H = require('./name-data/helper');

var CULTURES = [
  'anglo', 'franco', 'centraleuropean', 'nordic', 'slavic', 'latin', 'latinamerican',
  'middleeastern', 'southasia', 'eastasia', 'centralasian', 'berber', 'african'
];

var DATA = {};
var i, key, mod, dir = path.join(__dirname, 'name-data');

for (i = 0; i < CULTURES.length; i++) {
  key = CULTURES[i];
  mod = require(path.join(dir, key + '.js'));
  DATA[key] = H.normalizeCulture(mod, 100);
}

var aliases = {
  french: 'franco',
  arabic: 'middleeastern',
  latino: 'latinamerican',
  european: 'centraleuropean',
  germanic: 'centraleuropean',
  africa: 'african',
  southasia: 'southasia',
  eastasia: 'eastasia'
};
Object.keys(aliases).forEach(function (alias) {
  DATA[alias] = DATA[aliases[alias]];
});

var out = path.join(__dirname, 'name-pool-data.js');
fs.writeFileSync(
  out,
  '/* Generated by build-name-data.js — do not edit by hand */\nvar NG_REAL=' +
    JSON.stringify(DATA) +
    ';\n',
  'utf8'
);

console.log('Wrote', out);
CULTURES.forEach(function (c) {
  console.log(
    c,
    'M', DATA[c].M.first.length, DATA[c].M.last.length,
    'F', DATA[c].F.first.length, DATA[c].F.last.length
  );
});
